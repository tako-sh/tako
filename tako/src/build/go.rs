use std::fs;
use std::path::Path;

const TAKO_GEN_HEADER: &str = "// Code generated by tako generate. DO NOT EDIT.\n";

/// Build the `tako_secrets.go` content with typed secret accessors.
fn build_gen_go(package_name: &str, secret_names: &[String]) -> String {
    let mut out = String::from(TAKO_GEN_HEADER);
    out.push_str(&format!("package {package_name}\n"));

    if secret_names.is_empty() {
        out.push_str(
            r#"
// Secrets provides type-safe access to Tako-managed secrets.
// No secrets configured — run `tako secret set <name> <value>` to add secrets,
// then `tako generate` to regenerate this file.
var Secrets = struct{}{}
"#,
        );
    } else {
        out.push_str("\nimport \"tako.sh\"\n\n");
        out.push_str("// Secrets provides type-safe access to Tako-managed secrets.\n");
        out.push_str("var Secrets = struct {\n");
        for name in secret_names {
            if !is_valid_secret_name(name) {
                continue;
            }
            let method = screaming_snake_to_pascal(name);
            out.push_str(&format!("\t{method} func() string\n"));
        }
        out.push_str("}{\n");
        for name in secret_names {
            if !is_valid_secret_name(name) {
                continue;
            }
            let method = screaming_snake_to_pascal(name);
            let escaped = name.replace('\\', "\\\\").replace('"', "\\\"");
            out.push_str(&format!(
                "\t{method}: func() string {{ return tako.GetSecret(\"{escaped}\") }},\n"
            ));
        }
        out.push_str("}\n");
    }

    out
}

/// Validate that a secret name is safe to use in generated Go code.
/// Only allows ASCII alphanumeric and underscores.
fn is_valid_secret_name(name: &str) -> bool {
    !name.is_empty()
        && name.chars().all(|c| c.is_ascii_alphanumeric() || c == '_')
        && name
            .chars()
            .next()
            .is_some_and(|c| c.is_ascii_alphabetic() || c == '_')
}

/// Convert `SCREAMING_SNAKE_CASE` to `PascalCase`.
///
/// Examples:
/// - `DATABASE_URL` → `DatabaseUrl`
/// - `API_KEY` → `ApiKey`
/// - `EXAMPLE_SECRET` → `ExampleSecret`
/// - `DB` → `Db`
fn screaming_snake_to_pascal(name: &str) -> String {
    name.split('_')
        .filter(|part| !part.is_empty())
        .map(|part| {
            let mut chars = part.chars();
            match chars.next() {
                Some(first) => {
                    let mut s = first.to_uppercase().to_string();
                    s.extend(chars.map(|c| c.to_lowercase().next().unwrap_or(c)));
                    s
                }
                None => String::new(),
            }
        })
        .collect()
}

/// Detect the Go package name from existing `.go` files in the directory.
/// Falls back to `"main"` if no files found.
fn detect_package_name(project_dir: &Path) -> String {
    let entries = match fs::read_dir(project_dir) {
        Ok(e) => e,
        Err(_) => return "main".to_string(),
    };

    for entry in entries.flatten() {
        let path = entry.path();
        if path.extension().is_some_and(|ext| ext == "go")
            && path
                .file_name()
                .is_some_and(|name| !name.to_string_lossy().ends_with("_test.go"))
            && path
                .file_name()
                .is_some_and(|name| name != "tako_secrets.go")
            && let Ok(content) = fs::read_to_string(&path)
        {
            for line in content.lines() {
                let line = line.trim();
                if let Some(rest) = line.strip_prefix("package ") {
                    let pkg = rest.split_whitespace().next().unwrap_or("main");
                    return pkg.to_string();
                }
            }
        }
    }

    "main".to_string()
}

/// Write `tako_secrets.go` to the project directory.
///
/// Returns `true` if the file was written (created or updated),
/// `false` if it already existed with the same content.
pub fn write_secret_accessors(project_dir: &Path) -> std::io::Result<bool> {
    let secret_names = read_secret_names(project_dir);
    write_secret_accessors_with_secrets(project_dir, &secret_names)
}

/// Write `tako_secrets.go` with explicit secret names.
pub fn write_secret_accessors_with_secrets(
    project_dir: &Path,
    secret_names: &[String],
) -> std::io::Result<bool> {
    let package_name = detect_package_name(project_dir);
    let path = project_dir.join("tako_secrets.go");
    let content = build_gen_go(&package_name, secret_names);
    if path.is_file() {
        let existing = fs::read_to_string(&path)?;
        if existing == content {
            return Ok(false);
        }
    }
    fs::write(&path, &content)?;
    Ok(true)
}

/// Read secret names from `.tako/secrets.json`.
///
/// Prefers "development" env, falls back to union of all envs.
fn read_secret_names(project_dir: &Path) -> Vec<String> {
    let secrets_path = project_dir.join(".tako").join("secrets.json");
    let content = match fs::read_to_string(&secrets_path) {
        Ok(c) => c,
        Err(_) => return Vec::new(),
    };

    let parsed: serde_json::Value = match serde_json::from_str(&content) {
        Ok(v) => v,
        Err(_) => return Vec::new(),
    };

    let obj = match parsed.as_object() {
        Some(o) => o,
        None => return Vec::new(),
    };

    if let Some(dev) = obj.get("development") {
        extract_secret_names(dev)
    } else {
        let mut all: Vec<String> = obj.values().flat_map(extract_secret_names).collect();
        all.sort();
        all.dedup();
        all
    }
}

fn extract_secret_names(env_value: &serde_json::Value) -> Vec<String> {
    let secrets = env_value.get("app").and_then(|s| s.as_object());
    match secrets {
        Some(map) => {
            let mut names: Vec<String> = map.keys().cloned().collect();
            names.sort();
            names
        }
        None => Vec::new(),
    }
}

#[cfg(test)]
mod tests {
    use super::*;
    use tempfile::TempDir;

    #[test]
    fn screaming_snake_to_pascal_converts_correctly() {
        assert_eq!(screaming_snake_to_pascal("DATABASE_URL"), "DatabaseUrl");
        assert_eq!(screaming_snake_to_pascal("API_KEY"), "ApiKey");
        assert_eq!(screaming_snake_to_pascal("EXAMPLE_SECRET"), "ExampleSecret");
        assert_eq!(screaming_snake_to_pascal("DB"), "Db");
        assert_eq!(screaming_snake_to_pascal("A_B_C"), "ABC");
    }

    #[test]
    fn build_gen_go_empty_secrets() {
        let content = build_gen_go("main", &[]);
        assert!(content.contains("package main"));
        assert!(content.contains("var Secrets = struct{}{}"));
        assert!(!content.contains("import"));
    }

    #[test]
    fn build_gen_go_with_secrets() {
        let names = vec!["API_KEY".to_string(), "DATABASE_URL".to_string()];
        let content = build_gen_go("main", &names);
        assert!(content.contains("package main"));
        assert!(content.contains("import \"tako.sh\""));
        assert!(content.contains("ApiKey func() string"));
        assert!(content.contains("DatabaseUrl func() string"));
        assert!(content.contains("tako.GetSecret(\"API_KEY\")"));
        assert!(content.contains("tako.GetSecret(\"DATABASE_URL\")"));
    }

    #[test]
    fn write_secret_accessors_creates_file() {
        let dir = TempDir::new().unwrap();
        let written = write_secret_accessors(dir.path()).unwrap();
        assert!(written);

        let content = fs::read_to_string(dir.path().join("tako_secrets.go")).unwrap();
        assert!(content.contains("Code generated by tako generate"));
        assert!(content.contains("package main"));
    }

    #[test]
    fn write_secret_accessors_is_idempotent() {
        let dir = TempDir::new().unwrap();
        assert!(write_secret_accessors(dir.path()).unwrap());
        assert!(!write_secret_accessors(dir.path()).unwrap());
    }

    #[test]
    fn write_secret_accessors_detects_package_name() {
        let dir = TempDir::new().unwrap();
        fs::write(
            dir.path().join("main.go"),
            "package myapp\n\nfunc main() {}\n",
        )
        .unwrap();
        write_secret_accessors(dir.path()).unwrap();

        let content = fs::read_to_string(dir.path().join("tako_secrets.go")).unwrap();
        assert!(content.contains("package myapp"));
    }

    #[test]
    fn write_secret_accessors_reads_secrets() {
        let dir = TempDir::new().unwrap();
        let secrets_dir = dir.path().join(".tako");
        fs::create_dir_all(&secrets_dir).unwrap();
        fs::write(
            secrets_dir.join("secrets.json"),
            r#"{
                "development": {
                    "key_id": "0123456789abcdef",
                    "app": {
                        "DB_URL": "encrypted",
                        "API_KEY": "encrypted"
                    }
                }
            }"#,
        )
        .unwrap();

        write_secret_accessors(dir.path()).unwrap();

        let content = fs::read_to_string(dir.path().join("tako_secrets.go")).unwrap();
        assert!(content.contains("ApiKey func() string"));
        assert!(content.contains("DbUrl func() string"));
        assert!(content.contains("tako.GetSecret(\"API_KEY\")"));
        assert!(content.contains("tako.GetSecret(\"DB_URL\")"));
    }

    #[test]
    fn detect_package_name_defaults_to_main() {
        let dir = TempDir::new().unwrap();
        assert_eq!(detect_package_name(dir.path()), "main");
    }

    #[test]
    fn detect_package_name_ignores_test_files() {
        let dir = TempDir::new().unwrap();
        fs::write(dir.path().join("main_test.go"), "package myapp_test\n").unwrap();
        fs::write(dir.path().join("app.go"), "package myapp\n").unwrap();
        assert_eq!(detect_package_name(dir.path()), "myapp");
    }
}
