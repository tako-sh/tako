#[test]
fn write_tako_declarations_creates_tako_dts_with_no_secrets() {
    let dir = TempDir::new().unwrap();
    let written = write_tako_declarations(dir.path()).unwrap();
    assert!(written);

    let content = fs::read_to_string(dir.path().join("tako.d.ts")).unwrap();
    assert!(
        content.starts_with(
            "/* prettier-ignore-start */\n/* eslint-disable */\n/* oxlint-disable */\n"
        )
    );
    assert!(content.contains("AUTO-GENERATED BY TAKO"));
    assert!(content.contains("`tako generate`"));
    assert!(content.contains(r#"import "tako.sh";"#));
    assert!(content.contains(r#"declare module "tako.sh""#));
    assert!(content.contains("export interface TakoSecrets"));
    assert!(content.contains(r#"export type Env = "development" | "production";"#));
    assert!(content.contains("interface TakoTypeRegistry"));
    assert!(!content.contains("TakoGeneratedEnv"));
    assert!(!content.contains("export const env"));
    assert!(!content.contains("export const secrets"));
}

